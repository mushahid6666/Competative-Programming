import sys, copy
class Solution(object):

    def updateMatrix(self, matrix):
        """
        :type matrix: List[List[int]]
        :rtype: List[List[int]]
        """
        if len(matrix) == 0:
            return matrix

        dist_matrix = [[sys.maxint for _ in range(len(matrix[0]))] for _ in range(len(matrix))]
        queue = []
        for i in range(len(dist_matrix)):
            for j in range(len(dist_matrix[0])):
                if matrix[i][j] == 0:
                    dist_matrix[i][j]=0
                    queue.append([i,j])
        neighbour_index = [[-1, 0], [0, -1] , [1, 0], [0, 1]]
        while len(queue)!= 0:
            new_index= queue.pop(0)
            cur_i, cur_j = new_index[0], new_index[1]
            for neighbour in neighbour_index:
                new_i, new_j = cur_i+ neighbour[0] , cur_j + neighbour[1]
                if new_i >=0 and new_j >=0 and new_i < len(matrix) and new_j < len(matrix[0]):
                    if dist_matrix[new_i][new_j] > (dist_matrix[cur_i][cur_j] + 1):
                        dist_matrix[new_i][new_j] = dist_matrix[cur_i][cur_j] + 1
                        queue.append([new_i, new_j])
        return dist_matrix


